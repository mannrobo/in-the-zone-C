void autoLS(const string motion, int time) {
		if(motion == "up") {
				motor[rightLS] = 90;
				motor[leftLS] = 80;
				wait1Msec(time);
		}
		if(motion == "down") {
				motor[rightLS] = -90;
				motor[leftLS] = -80;
				wait1Msec(time);
		}
		autoLSStop();
}

void stopDrive() {
		motor[bottomRight] = 0;
		motor[bottomLeft] = 0;
		motor[topRight] = 0;
		motor[topLeft] = 0;
}

void autoDrive(const string direction, int speed, int ticks) {
		if(direction == "forward") {
				motor[bottomRight] = speed;
				motor[topRight] = speed;
				motor[bottomLeft] = speed;
				motor[topLeft] = speed;
				waitTicks(ticks);
		}
		if(direction == "backward") {
				motor[bottomRight] = -speed;
				motor[topRight] = -speed;
				motor[bottomLeft] = -speed;
				motor[topLeft] = -speed;
				waitTicks(ticks);
		}
		if(direction == "right") {
				motor[bottomRight] = -speed;
				motor[topRight] = -speed;
				motor[bottomLeft] = speed;
				motor[topLeft] = speed;
				waitTicks(ticks);
		}
		if(direction == "left") {
				motor[bottomRight] = speed;
				motor[topRight] = speed;
				motor[bottomLeft] = -speed;
				motor[topLeft] = -speed;
				waitTicks(ticks);
		}
		stopDrive();
}

